    <h1>運動管理學類</h1>
    
    <h4>這個學類學什麼？</h4>
    <p>運動管理是指運動組織內的管理人員，運用一般管理的理論、技術與方法，經由規劃、組織、人員晉用、領導和管制等五大功能的發揮，並善用組織人力、物力和財力等資源，以達成經營運動事業，提供大眾參與運動、觀賞運動或提供運動服務的一種動態的過程。
        學習內容包含管理學基礎、運動科學基礎、運動管理基礎、資訊與媒體基礎、運動健康指導、運動治療、運動健康行銷企劃等領域。
        如果你學習運動管理學類，你除了可以學會如何籌劃與營運各大運動競賽或體育組織，例如籌辦世界大學運動會（世大運）、全大運、經營健身工廠等。</p>
    
    <h4>相似學類比一比</h4>
    <p>本學類主要以組織人力、物力和財力等資源，達成經營運動事業，針對場館設施、運動賽事、運動課程提供推廣及服務，屬組織管理性質。
        本學類容易與體育、休閒管理學類搞混。
        體育學類：重視實作與應用層面，對各學科的理論意涵做正確理解，培育體育運動領域全方位及領導人才。
        休閒管理學類：針對民眾在休閒時進行的戶外／室內活動，提供企劃與執行的方案，並開發、經營、管理休閒活動所進行之場所或商務行為，偏重休閒觀光、遊憩規劃與行銷。
        同為運動相關學類，與運動相關人員皆有所接觸，也須了解各種運動型態狀況，以達到精準服務的目標。
        </p>
    
    <h4>就要你這款</h4>
    <p>就要你具有積極熱血、動作迅速敏捷且樂於群體合作，且對於運動賽事感到興趣，並且能夠喜歡為協助運動團體建立一個運作計畫與機制，會想要學習與健身、商務、法律等等的相關知識，你就是適合運動管理學類的人喔。</p>
    
    <h4>你所不知道的大學生活</h4>
    <ul>
        <li>運動管理學、經濟學、運動資訊管理、運動傳播等課程扎實以提升管理能力及行銷規劃能力</li>
        <li>瞭解人體各個部位，學習基礎運動知識及技能，如人體解剖學、運動生理學、運動心理學</li>
        <li>為能準確辦理各類運動賽事及課程，需了解各類運動基礎規則及技能</li>
        <li>學習如何統籌及推廣運動賽事、運動中心、運動課程，並學習體育組織相關法律問題</li>
        <li>將所學技能及知識，運用在實務上，進入運動相關產業進行實習課程，及早為投入職場準備</li>
    </ul>

    <h4>興趣類型</h4>
    <p>
        文藝傳播(SA) 、 教育訓練(SE) 、 服務經營(ES) 、 健康促進(SR) 、 資訊安全(CR)
    </p>

    <h4>不可不知的知識領域</h4>
    <p>管理 、 本國語文 、 教育訓練 、 人群服務</p>

    <h4>必修或核心課程？</h4>
    <ul>
        <li>體育領域：人體解剖學、運動心理學、運動生理學</li>
        <li>管理領域：運動組織與行政、運動賽會管理、運動行銷管理、財務管理</li>
        
    </ul>
